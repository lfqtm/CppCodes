#include <iostream>
using namespace std;

int main() {

    //整数输入
    int a = 0;
    cout << "请输入整数变量a:" << endl;
    cin >> a;
    cout << a << endl;

    //浮点数输入
    double d = 0.0;
    cout << "请输入浮点数变量d:" << endl;
    cin >> d;
    cout << d << endl;

    //字符型输入
    char ch = 'a';
    cout << "请输入字符型变量ch:" << endl;
    cin >> ch;
    cout << ch << endl;

    //布尔
    bool b = true;
    cout << "请输入布尔变量b:" << endl;
    cin >> b;
    cout << b << endl;

    return 0;
}