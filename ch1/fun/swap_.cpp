#include "swap_.h"

//值传递不会影响实参
void swap(int num1, int num2) {
    cout << "交换前：num1：" << num1 << "\t num2：" << num2 << endl;
    int temp = num1;
    num1 = num2;
    num2 = temp;
    cout << "交换前：num1：" << num1 << "\t num2：" << num2 << endl;
}

int main() {
    int a = 10;
    int b = 20;
    cout << a << "===" << b << endl;
    swap(a, b);
    cout << a << "===" << b << endl;
    return 0;
}